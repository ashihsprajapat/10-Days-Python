
# data type is name that store the address of value 
'''
some data type like
String -> str
Integer -> int
Float -> float
Boolean -> True, False (first Character is capital)
None

'''

name="Ashish Prajapat"
print(type(name), name) #<class 'str'> Ashish Prajapat

age=21
print(type(age), age) #<class 'int'> 21

marks= 90.99
print(type(marks), marks) #<class 'float'> 90.99



